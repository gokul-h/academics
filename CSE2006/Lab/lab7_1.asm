DATA SEGMENT
	MSG DB "GOKUL","$"
	MSG1 DB "LENGTH:","$"
DATA ENDS

CODE SEGMENT
	ASSUME CS:CODE, DS:DATA
	START:
	MOV AX,DATA
	MOV DS,AX
	LEA SI,MSG
	MOV CX,0000H
	MOV AL,"$"
	REP1:
		MOV AH,[SI]
		CMP AL,AH
		JE OUT1
		INC CX
		INC SI
	JMP REP1
	OUT1:
		LEA DX,MSG1
		MOV AH,09H
		INT 21H
		MOV DX,0000H
		MOV DL,CL
		ADD DL,30H 
		MOV AH,02H
		INT 21H
		MOV AH,4CH
		INT 21H
	HLT
CODE ENDS
    END START
	